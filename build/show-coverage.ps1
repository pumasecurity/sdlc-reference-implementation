# Display test coverage summary from test results
# This script helps visualize coverage information

param(
    [string]$TestResultsPath = "test-results"
)

Write-Host ""
Write-Host "========================================" -ForegroundColor Cyan
Write-Host "CODE COVERAGE SUMMARY" -ForegroundColor Cyan
Write-Host "========================================" -ForegroundColor Cyan
Write-Host ""

# Check if test results directory exists
if (-not (Test-Path $TestResultsPath)) {
    Write-Host "Test results directory not found: $TestResultsPath" -ForegroundColor Yellow
    Write-Host ""
    Write-Host "Please run tests first:" -ForegroundColor Gray
    Write-Host "  .\build\build.ps1" -ForegroundColor Cyan
    Write-Host "  .\build\run-tests.ps1" -ForegroundColor Cyan
    exit 0
}

# Find coverage files (both .coverage and cobertura.xml)
$coverageFiles = Get-ChildItem -Path $TestResultsPath -Filter "*.coverage" -Recurse -ErrorAction SilentlyContinue
$coberturaFiles = Get-ChildItem -Path $TestResultsPath -Filter "coverage.cobertura.xml" -Recurse -ErrorAction SilentlyContinue
$xmlFiles = Get-ChildItem -Path $TestResultsPath -Filter "*.xml" -Recurse -ErrorAction SilentlyContinue | Where-Object { $_.Name -match "coverage" -and $_.Name -notlike "*.cobertura.xml" }

$allCoverageFiles = @()
$allCoverageFiles += $coverageFiles
$allCoverageFiles += $coberturaFiles
$allCoverageFiles += $xmlFiles

if ($allCoverageFiles.Count -eq 0) {
    Write-Host "No coverage files found in $TestResultsPath" -ForegroundColor Yellow
    Write-Host ""
    Write-Host "Coverage files are typically:" -ForegroundColor Gray
    Write-Host "  - coverage.cobertura.xml (Cobertura XML format - generated by Coverlet)" -ForegroundColor Gray
    Write-Host "  - .coverage (Visual Studio binary format)" -ForegroundColor Gray
    Write-Host ""
    Write-Host "Make sure you ran tests with coverage collection:" -ForegroundColor Gray
    Write-Host "  .\build\run-tests.ps1" -ForegroundColor Cyan
    Write-Host ""
    Write-Host "Check that dotnet CLI is available and coverlet.collector is installed" -ForegroundColor Gray
    exit 0
}

Write-Host "Found $($allCoverageFiles.Count) coverage file(s):" -ForegroundColor Green
Write-Host ""

foreach ($file in $allCoverageFiles) {
    $sizeKB = [math]::Round($file.Length / 1KB, 2)
    $icon = if ($file.Extension -eq ".xml") { "??" } else { "??" }
    Write-Host "  $icon $($file.Name)" -ForegroundColor Cyan
    Write-Host "     Location: $($file.FullName)" -ForegroundColor Gray
    Write-Host "     Size: $sizeKB KB" -ForegroundColor Gray
    Write-Host "     Created: $($file.LastWriteTime)" -ForegroundColor Gray
    
    # Try to parse Cobertura XML for summary
    if ($file.Name -like "*.cobertura.xml") {
        try {
            [xml]$xml = Get-Content $file.FullName
            $lineRate = $xml.coverage.'line-rate'
            $branchRate = $xml.coverage.'branch-rate'
            $linesCovered = $xml.coverage.'lines-covered'
            $linesValid = $xml.coverage.'lines-valid'
            
            if ($lineRate) {
                $linePercent = [math]::Round([double]$lineRate * 100, 2)
                Write-Host "     Line Coverage: $linePercent% ($linesCovered/$linesValid lines)" -ForegroundColor Green
            }
            if ($branchRate) {
                $branchPercent = [math]::Round([double]$branchRate * 100, 2)
                Write-Host "     Branch Coverage: $branchPercent%" -ForegroundColor Green
            }
        }
        catch {
            Write-Host "     (Could not parse coverage metrics)" -ForegroundColor Gray
        }
    }
    
    Write-Host ""
}

Write-Host "========================================" -ForegroundColor Cyan
Write-Host "How to View Coverage:" -ForegroundColor Yellow
Write-Host "========================================" -ForegroundColor Cyan
Write-Host ""
Write-Host "1. Cobertura XML Files (.cobertura.xml):" -ForegroundColor White
Write-Host "   - Can be viewed in text editors" -ForegroundColor Gray
Write-Host "   - Compatible with many CI/CD tools" -ForegroundColor Gray
Write-Host "   - Use ReportGenerator to create HTML reports" -ForegroundColor Gray
Write-Host ""
Write-Host "2. Visual Studio Binary (.coverage):" -ForegroundColor White
Write-Host "   - Download from pipeline artifacts" -ForegroundColor Gray
Write-Host "   - Open in Visual Studio: File > Open > File..." -ForegroundColor Gray
Write-Host "   - View detailed line-by-line coverage" -ForegroundColor Gray
Write-Host ""
Write-Host "3. Generate HTML Report (optional):" -ForegroundColor White
Write-Host "   - Install: dotnet tool install -g dotnet-reportgenerator-globaltool" -ForegroundColor Gray
Write-Host "   - Run: reportgenerator -reports:**\*.cobertura.xml -targetdir:coveragereport -reporttypes:Html" -ForegroundColor Gray
Write-Host ""
Write-Host "4. GitHub Actions:" -ForegroundColor White
Write-Host "   - Coverage files are stored as workflow artifacts" -ForegroundColor Gray
Write-Host "   - Download from the Actions tab on the workflow run" -ForegroundColor Gray
Write-Host "   - Artifacts are retained for 30 days" -ForegroundColor Gray
Write-Host ""

# Display JUnit XML files
$junitFiles = Get-ChildItem -Path $TestResultsPath -Filter "junit.xml" -Recurse -ErrorAction SilentlyContinue

if ($junitFiles -and $junitFiles.Count -gt 0) {
    Write-Host "========================================" -ForegroundColor Cyan
    Write-Host "Test Result Files:" -ForegroundColor Yellow
    Write-Host "========================================" -ForegroundColor Cyan
    Write-Host ""
    
    foreach ($file in $junitFiles) {
        Write-Host "  ? $($file.Name)" -ForegroundColor Green
        Write-Host "     Location: $($file.FullName)" -ForegroundColor Gray
        Write-Host "     Note: Test results are uploaded as GitHub Actions artifacts" -ForegroundColor Cyan
        Write-Host ""
    }
}

exit 0
